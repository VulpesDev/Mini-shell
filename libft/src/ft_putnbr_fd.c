#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	int	sign;

	sign = 1;
	if (n < 0)
	{
		sign = -1;
		write(fd, "-", 1);
	}
	if (n > 9 || n < -9)
		ft_putnbr_fd((n / 10) * sign, fd);
	write(fd, &"0123456789"[(n % 10) * sign], 1);
}
